рядки = [
    'абвба', 'абввба', '123421', '78a87', 'дамблдор', '6666', '77777', 'FuFuF' * 10
]

for рядок in рядки:
    паліндром = True
    for i in range(len(рядок) // 2):
        if рядок[i] != рядок[- i - 1]:
            паліндром = False
            break
    результат = '' if паліндром else 'не '
    print(f'{рядок}: {результат}паліндром')
