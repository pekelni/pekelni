рядок, лічильник_чисел_у_рядку = '', 0
for число in range(2, 1001):
    просте = True
    for дільник in range(2, число):
        if число % дільник == 0:
            просте = False
            break
    if not просте:
        continue
    рядок += str(число) + ' '
    лічильник_чисел_у_рядку += 1
    if лічильник_чисел_у_рядку == 15:
        print(рядок)
        рядок, лічильник_чисел_у_рядку = '', 0

if рядок:
    print(рядок)
