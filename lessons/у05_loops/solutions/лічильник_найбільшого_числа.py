найбільше_число, лічильник = None, 0
while True:
    число = input('')
    if not число:
        break
    число = int(число)
    if not найбільше_число or число > найбільше_число:
        найбільше_число = число
        лічильник = 1
    elif найбільше_число == число:
        лічильник += 1

print(f'Найбільше число: {найбільше_число}, зустрілося {лічильник} разів')
