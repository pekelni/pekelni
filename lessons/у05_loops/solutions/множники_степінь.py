число = int(input('Введіть ціле додатнє число: '))
for множник in range(2, число + 1):
    степінь = 0
    while число % множник == 0:
        число //= множник
        степінь += 1
    if степінь:
        степінь = f'**{степінь}' if степінь > 1 else ''
        print(f'{множник}{степінь}', end=' ')
    if число == 1:
        break
