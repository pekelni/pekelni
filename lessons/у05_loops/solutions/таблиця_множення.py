ПОЧАТОК_ДРУГОЇ_КОЛОНКИ = 17

for i in range(1, 6):
    for j in range(1, 11):
        перший_приклад = f'{i} * {j} = {i * j}'
        пробіли = ' ' * (ПОЧАТОК_ДРУГОЇ_КОЛОНКИ - len(перший_приклад))

        друге_і = i + 5
        другий_приклад = f'{друге_і} * {j} = {друге_і * j}'
        print(перший_приклад + пробіли + другий_приклад)
    print()
